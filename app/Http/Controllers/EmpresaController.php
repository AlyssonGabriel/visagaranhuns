            'status' => 'required|string',
            'tipo' => 'required|string',
            'email' => 'required|email',
            'password' => 'required',
        ]);

        $user = User::create([
            'name' => $request->name,
            'email' => $request->email,
            'password' => bcrypt($request->password),
            'tipo' => "empresa",
        ]);

        $empresa = Empresa::create([
            'cnpjcpf' => $request->cnpjcpf,
            'status' => $request->status,
            'tipo' => $request->tipo,
            'user_id' => $user->id,
        ]);

        return redirect()->route('home');
    }

